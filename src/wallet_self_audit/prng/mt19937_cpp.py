def entropy_from_timestamp(timestamp: int, n_words: int = 12) -> bytes:
    """Reproduce ``bx seed`` flow: one timestamp → entropy bytes.

    Args:
        timestamp: 32-bit Unix timestamp (used as the single seed_seq input).
        n_words: BIP-39 word count (12=128 bits, 24=256 bits).

    Returns:
        Entropy bytes (16 for 12 words, 32 for 24 words). Big-endian-packed
        from successive ``next_uint32()`` outputs of an ``std::mt19937``
        seeded by ``std::seed_seq{timestamp}``.

    Raises:
        ValueError: If ``n_words`` is not a supported BIP-39 length.
    """
    if n_words == 12:
        out_count = 4  # 4 * 32 = 128 bits
    elif n_words == 15:
        out_count = 5
    elif n_words == 18:
        out_count = 6
    elif n_words == 21:
        out_count = 7
    elif n_words == 24:
        out_count = 8
    else:
        raise ValueError(f"unsupported n_words={n_words}; expected 12/15/18/21/24")

    rng = MT19937.from_seed_seq([timestamp & _UINT32_MASK])
    out = bytearray()
    for _ in range(out_count):
        out += rng.next_uint32().to_bytes(4, "big")
    return bytes(out)


