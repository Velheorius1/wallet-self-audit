def _coerce_fixture_cases(raw: object) -> list[dict[str, object]] | None:
    """Validate that a parsed JSON object has the expected fixture shape."""
    if not isinstance(raw, dict):
        return None
    raw_dict: dict[str, object] = raw  # pyright: ignore[reportUnknownVariableType,reportAssignmentType]
    cases_obj = raw_dict.get("cases")
    if not isinstance(cases_obj, list):
        return None
    out: list[dict[str, object]] = []
    for case in cases_obj:  # pyright: ignore[reportUnknownVariableType]
        if not isinstance(case, dict):
            return None
        out.append(case)  # pyright: ignore[reportUnknownArgumentType]
    return out


def _load_fixture() -> list[dict[str, object]] | None:
    """Load the in-tree std::mt19937 fixture if present.

    Look in two places: the package resources (after install) and the source
    repo (during dev). Return ``None`` if the file is missing — this is a
    soft error that ``check_fixtures()`` upgrades into a hard failure.
    """
    repo_root = Path(__file__).resolve().parents[3]
    for path_candidate in [
        Path(__file__).resolve().parent / "fixtures" / "mt19937_cpp_ref.json",
        repo_root / "tests" / "fixtures" / "prng" / "mt19937_cpp_ref.json",
    ]:
        if path_candidate.exists():
            raw_data: object = json.loads(path_candidate.read_text(encoding="utf-8"))
            cases = _coerce_fixture_cases(raw_data)
            if cases is not None:
                return cases
    try:
        with (
            resources.files("wallet_self_audit.prng.fixtures")
            .joinpath("mt19937_cpp_ref.json")
            .open("rb") as f
        ):
            raw_data2: object = json.loads(f.read().decode("utf-8"))
            cases2 = _coerce_fixture_cases(raw_data2)
            if cases2 is not None:
                return cases2
    except (FileNotFoundError, ModuleNotFoundError, AttributeError):
        return None
    return None


def check_fixtures() -> bool:
    """Validate this implementation against the in-tree fixture.

    Returns:
        ``True`` if all cases pass. ``False`` if the fixture is absent
        (Milk Sad scan should refuse to run in that case — a missing
        validation oracle is worse than a wrong answer).

    Raises:
        AssertionError: If the implementation diverges from a reference
            output. This is a hard failure: never ship a Milk Sad scanner
            whose mt19937 is wrong.
    """
    cases = _load_fixture()
    if cases is None:
        return False

    for case in cases:
        seed_seq_input_obj = case.get("seed_seq_input")
        expected_outputs_obj = case.get("first_outputs")
        if not isinstance(seed_seq_input_obj, list) or not isinstance(expected_outputs_obj, list):
            raise TypeError("malformed fixture entry: missing seed_seq_input / first_outputs")
        seed_seq_input: list[int] = [
            int(x)
            for x in seed_seq_input_obj  # pyright: ignore[reportUnknownArgumentType,reportUnknownVariableType]
        ]
        expected_outputs: list[int] = [
            int(x)
            for x in expected_outputs_obj  # pyright: ignore[reportUnknownArgumentType,reportUnknownVariableType]
        ]

        rng = MT19937.from_seed_seq(seed_seq_input)
        actual = [rng.next_uint32() for _ in range(len(expected_outputs))]
        if actual != expected_outputs:
            raise AssertionError(
                f"mt19937 fixture mismatch for seed_seq_input={seed_seq_input}: "
                f"got {actual}, expected {expected_outputs}"
            )
    return True


